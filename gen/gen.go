package gen

import (
	"bytes"
	"fmt"
	"strconv"
	"strings"

	"github.com/ddkwork/golibrary/stream"
)

type GeneratedFile struct{ buf bytes.Buffer }

func New() Interface { return &GeneratedFile{} }

func (g *GeneratedFile) P(v ...any) {
	for _, x := range v {
		fmt.Fprint(&g.buf, x)
	}
	fmt.Fprintln(&g.buf)
}

func (g *GeneratedFile) Enum(kindName string, kinds, tooltip []string) {
	for i, kind := range kinds {
		kinds[i] = stream.ToCamelUpper(kind, false)
	}
	kindNameUpper := stream.ToCamelUpper(kindName, false)
	InvalidKind := "Invalid" + kindNameUpper + "Kind" // InvalidAppKind

	g.P("package ", stream.GetPackageName())
	g.P("")

	g.P("import (")
	g.P(strconv.Quote("cogentcore.org/core/gi"))
	g.P(strconv.Quote("cogentcore.org/core/icons"))
	g.P(strconv.Quote("golang.org/x/exp/constraints"))
	g.P(")")
	g.P("")

	g.P("// Code generated by GeneratedFile enum - DO NOT EDIT.")

	g.P("")
	g.P("type ", kindNameUpper, "Kind byte")
	g.P("")
	g.P("const (")
	for i, kind := range kinds {
		if i == 0 {
			g.P(" ", kind, "Kind", " ", kindNameUpper, "Kind = iota")
			continue
		}
		g.P(" "+kind, "Kind")
	}
	g.P(" ", InvalidKind)
	// kinds = append(kinds, InvalidKind)
	g.P(")")
	g.P("")

	g.P("func ConvertInteger2", kindNameUpper, "Kind[T constraints.Integer](v T)", kindNameUpper, "Kind  {")
	g.P("  return ", kindNameUpper, "Kind(v)")
	g.P("}")
	g.P("")

	g.P("func (k ", kindNameUpper, "Kind) AssertKind(kinds string) ", kindNameUpper, "Kind {")
	g.P(" for _, kind := range k.Kinds() {")
	g.P("  if kinds == kind.String() {")
	g.P("   return kind")
	g.P("  }")
	g.P(" }")
	g.P(" return " + InvalidKind)
	g.P("}")
	g.P("")

	g.P("func (k ", kindNameUpper, "Kind) ChooserItem() []gi.ChooserItem {")
	g.P("chooserItems := make([]gi.ChooserItem, 0)")
	g.P("for _, kind := range k.Kinds() {")
	g.P("chooserItems = append(chooserItems, gi.ChooserItem{")
	g.P("Value:           kind.String(),")
	g.P("Label:           \"\",")
	g.P("Icon:            icons.Icon(k.SvgFileName()),")
	if len(tooltip) > 0 {
		g.P("Tooltip:         kind.Tooltip(),")
	} else {
		g.P("Tooltip:          kind.String(),")
	}
	g.P("Func:            nil,")
	g.P("SeparatorBefore: false,")
	g.P("})")
	g.P("}")
	g.P("return chooserItems")
	g.P("}")
	g.P("")

	g.P("func (k ", kindNameUpper, "Kind) String() string {")
	g.P(" switch k {")
	for _, kind := range kinds {
		g.P(" case ", kind, "Kind:")
		g.P("  return ", strconv.Quote(kind))
	}
	g.P(" default:")
	g.P("  return ", strconv.Quote(InvalidKind))
	g.P(" }")
	g.P("}")
	g.P("")

	if len(tooltip) > 0 {
		g.P("func (k ", kindNameUpper, "Kind) Tooltip() string {")
		g.P(" switch kind {")
		for i, kind := range kinds {
			g.P(" case ", kind, "Kind:")
			g.P("  return ", strconv.Quote(tooltip[i]))
		}
		g.P(" default:")
		g.P("  return ", strconv.Quote(InvalidKind))
		g.P(" }")
		g.P("}")
		g.P("")
	}

	g.P("func (k ", kindNameUpper, "Kind) Keys() []string {")
	g.P(" return []string{")
	for _, kind := range kinds {
		g.P("  ", strconv.Quote(kind), ",")
	}
	g.P("  ", strconv.Quote(InvalidKind), ",")
	g.P(" }")
	g.P("}")
	g.P("")

	g.P("func (k ", kindNameUpper, "Kind) Kinds() []", kindNameUpper, "Kind {")
	g.P(" return []", kindNameUpper, "Kind{")
	for _, kind := range kinds {
		g.P("  ", kind, "Kind,")
	}
	g.P("  ", InvalidKind, ",")
	g.P(" }")
	g.P("}")
	g.P("")

	g.P("func (k ", kindNameUpper, "Kind) SvgFileName() string {")
	g.P(" switch k {")
	for _, kind := range kinds {
		g.P(" case ", kind, "Kind:")
		g.P("  return ", strconv.Quote(kind)) // todo to low
	}
	g.P("	default:")
	g.P("  return ", strconv.Quote(InvalidKind))
	g.P(" }")
	g.P("}")
	g.P("")
	WriteGoFile(kindName+"_enum_gen.go", g.buf.String())
	g.buf.Reset()
}

func (g *GeneratedFile) FileAction() {
	g.P("package ", stream.GetPackageName())
	g.P("")

	g.P("import (")
	g.P(strconv.Quote("github.com/ddkwork/golibrary/mylog"))
	g.P(strconv.Quote("github.com/ddkwork/golibrary/safeType"))
	g.P(strconv.Quote("go/format"))
	g.P(strconv.Quote("os"))
	g.P(strconv.Quote("path/filepath"))
	g.P(")")
	g.P("")

	g.P("func WriteGoFile[T safeType.Type](name string, data T)  bool {")
	g.P(" s := safeType.New(data)")
	g.P(" source, err := format.Source(s.Bytes())")
	g.P(" if !mylog.Error(err) {")
	g.P("  return write(name, false, s.Bytes())")
	g.P(" }")
	g.P(" return write(name, false, source)")

	g.P("}")
	g.P("")

	g.P("func WriteAppend[T safeType.Type](name string, data T)  bool  { return write(name, true, data) }")
	g.P("func WriteTruncate[T safeType.Type](name string, data T)  bool  { return write(name, false, data) }")
	g.P("func WriteBinaryFile[T safeType.Type](name string, data T)  bool{ return write(name, false, data) }")
	g.P("")

	g.P("func write[T safeType.Type](name string, isAppend bool, data T) (ok bool) {")
	g.P(" if !CreatDirectory(filepath.Dir(name)) {")
	g.P("  return")
	g.P(" }")
	g.P(" fnCreateFile := func() (*os.File, error) {")
	g.P("  if isAppend {")
	g.P("   return os.OpenFile(name, os.O_RDWR|os.O_CREATE|os.O_APPEND, 0o666)")
	g.P("  }")
	g.P("  return os.Create(name) //== os.Truncate(name, 0)")
	g.P(" }")
	g.P(" f, err := fnCreateFile()")
	g.P(" if !mylog.Error(err) {")
	g.P("  switch {")
	g.P("  case os.IsExist(err):")
	g.P("   return write(name, isAppend, data)")
	g.P("  case os.IsNotExist(err):")
	g.P("   return write(name, isAppend, data)")
	g.P("  case os.IsPermission(err):")
	g.P("   return")
	g.P("  }")
	g.P(" }")
	g.P(" s := safeType.New(data)")
	g.P(" if !mylog.Error2(f.Write(s.Bytes())) {")
	g.P("  return")
	g.P(" }")
	g.P(" return mylog.Error(f.Close())")
	g.P("}")
	g.P("")

	g.P("func CreatDirectory(dir string) bool {")
	g.P(" fnMakeDir := func() bool { return mylog.Error(os.MkdirAll(dir, os.ModePerm)) }")
	g.P(" info, err := os.Stat(dir)")
	g.P(" switch {")
	g.P(" case os.IsExist(err):")
	g.P("  return true")
	g.P(" case os.IsNotExist(err):")
	g.P("  return fnMakeDir()")
	g.P(" case err == nil:")
	g.P("  return info.IsDir()")
	g.P(" default:")
	g.P("  return mylog.Error(err)")
	g.P(" }")
	g.P("}")

	WriteGoFile("fileAction_gen.go", g.buf.String())
	g.buf.Reset()
}

func (g *GeneratedFile) ReadTemplates(path string) {
	lines, ok := stream.NewReadFile(path).ToLines()
	if !ok {
		return
	}
	s := stream.New("")
	for _, line := range lines {
		needNewLine := false
		if line == "" {
			needNewLine = true
		}
		if strings.HasPrefix(line, "package") {
			line = "g.P(" + strconv.Quote("package ") + ", stream.GetPackageName())"
		} else {
			line = strings.ReplaceAll("g.P("+strconv.Quote(line)+")", `\t`, ` `)
			line = Unquote(line)
		}
		s.WriteStringLn(line)
		if needNewLine {
			s.NewLine()
		}
	}
	s.NewLine()
	s.WriteStringLn("WriteGoFile(" + strconv.Quote(stream.BaseName(path)+"_gen.go") + ", g.buf.String())")
	s.WriteStringLn("g.buf.Reset()")
	println(s.String())
}

func Unquote(line string) string {
	begin := strings.Index(line, `\"`)
	if begin < 0 {
		return line
	}
	split := strings.Split(line, `\"`)
	ss := stream.New("")
	for _, s := range split {
		if strings.Contains(s, `"`) {
			s = strings.ReplaceAll(s, `"`, ``)
			ss.WriteString(s)
		} else {
			ss.WriteString("strconv.Quote(")
			ss.WriteString(strconv.Quote(s))
			ss.WriteString(")")
		}
	}
	return ss.String()
}
